q1 = 3.556789
q2 = 456
print(f'1- q1: {q1}, q2: {q2}')
print("2- q1 {:.2f}".format(q1)) #string format
print("3- q2 {:6d}".format(q2))  #padding
print("4- q2 {:10d}".format(q2))  #padding
print("5- q1 {:7.2f}".format(q1)) #

